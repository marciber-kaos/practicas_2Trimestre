
entero = int(input("Ingresa un número entero (por ejemplo, 42): "))
decimal = float(input("Ingresa un número decimal (por ejemplo, 3.14): "))
texto = str(input("Ingresa un texto (por ejemplo, hola): "))
booleano = input("Ingresa True o False: ").strip().lower() == "true"



print(f"Un número entero (int), por ejemplo: {entero}")
print(f"Un número decimal (float), por ejemplo: {decimal}")
print(f"Un texto (str), por ejemplo: {texto}")
print(f"Un valor booleano (bool), por ejemplo: {booleano}")


